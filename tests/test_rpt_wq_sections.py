@unittest.skipUnless(WQ_RPT.exists(), f"WQ fixture not found at {WQ_RPT}")
class SubcatchmentWashoffSummaryTests(unittest.TestCase):
    @classmethod
    def setUpClass(cls) -> None:
        cls.rpt_text = WQ_RPT.read_text(encoding="utf-8")
        cls.rows = parse_variable_section(cls.rpt_text, SECTIONS["Subcatchment Washoff Summary"])

    def test_returns_four_subcatchments(self) -> None:
        """Smoke fixture has 4 subcatchments: S1, S2, S3, S4."""
        names = [r["name"] for r in self.rows]
        self.assertEqual(sorted(names), ["S1", "S2", "S3", "S4"])

    def test_row_has_name_and_loads_keys(self) -> None:
        for row in self.rows:
            self.assertIn("name", row)
            self.assertIn("loads", row)

    def test_tss_load_is_positive_for_all_subcatchments(self) -> None:
        for row in self.rows:
            self.assertIn("TSS", row["loads"])
            self.assertGreater(row["loads"]["TSS"], 0.0)

    def test_tss_loads_match_fixture_values(self) -> None:
        """Pin exact values from the PR1 engine smoke output."""
        expected = {"S1": 0.109, "S2": 0.094, "S3": 0.112, "S4": 0.062}
        actual = {r["name"]: r["loads"]["TSS"] for r in self.rows}
        for sub, exp_val in expected.items():
            self.assertIn(sub, actual)
            self.assertAlmostEqual(actual[sub], exp_val, places=3,
                                   msg=f"{sub} TSS load: expected {exp_val}, got {actual[sub]}")

    def test_system_row_excluded(self) -> None:
        names = [r["name"] for r in self.rows]
        self.assertNotIn("System", names)


