@unittest.skipUnless(WQ_RPT.exists(), f"WQ fixture not found at {WQ_RPT}")
class OutfallLoadingWQExtensionTests(unittest.TestCase):
    """Regression test: Outfall Loading Summary gains TSS column when WQ active."""

    @classmethod
    def setUpClass(cls) -> None:
        cls.rpt_text = WQ_RPT.read_text(encoding="utf-8")
        cls.rows = parse_section(cls.rpt_text, SECTIONS["Outfall Loading Summary"])

    def test_returns_one_outfall(self) -> None:
        self.assertEqual(len(self.rows), 1)
        self.assertEqual(self.rows[0]["node"], "OF1")

    def test_core_hydraulic_fields_present(self) -> None:
        row = self.rows[0]
        for field in ("node", "flow_freq_pct", "avg_flow", "max_flow",
                      "total_volume_10_6_ltr", "pollutant_loads"):
            self.assertIn(field, row)

    def test_tss_load_in_pollutant_loads(self) -> None:
        row = self.rows[0]
        self.assertIn("TSS", row["pollutant_loads"])
        self.assertAlmostEqual(row["pollutant_loads"]["TSS"], 0.524, places=3)

    def test_non_wq_outfall_backward_compat(self) -> None:
        """Non-WQ rpt: pollutant_loads is always present but empty."""
        non_wq_rpt = """\
  ***********************
  Outfall Loading Summary
  ***********************

  -----------------------------------------------------------
                         Flow       Avg       Max       Total
                         Freq      Flow      Flow      Volume
  Outfall Node           Pcnt       CMS       CMS    10^6 ltr
  -----------------------------------------------------------
  O1                    52.35     0.205     3.366      46.402
  O2                    30.00     0.100     1.500      12.100
  -----------------------------------------------------------
  System                82.35     0.305     4.866      58.502
"""
        rows = parse_section(non_wq_rpt, SECTIONS["Outfall Loading Summary"])
        self.assertEqual(len(rows), 2)
        for row in rows:
            self.assertIn("pollutant_loads", row)
            self.assertEqual(row["pollutant_loads"], {})
        # Core fields byte-identical to pre-WQ behaviour.
        self.assertAlmostEqual(rows[0]["max_flow"], 3.366, places=3)
        self.assertAlmostEqual(rows[1]["max_flow"], 1.500, places=3)


