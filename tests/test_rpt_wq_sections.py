"""Parser unit tests for the four SWMM 5.2.4 water-quality rpt sections.

Ground truth: ``tests/fixtures/wq/wq_smoke.rpt`` — generated by the SWMM 5.2.4
engine from a 1-pollutant (TSS), 4-subcatchment model (PR1 smoke fixture).

Sections under test
-------------------
* Runoff Quality Continuity   (parse_variable_section)
* Quality Routing Continuity  (parse_variable_section)
* Subcatchment Washoff Summary (parse_variable_section)
* Link Pollutant Load Summary  (parse_variable_section)
* Outfall Loading Summary WQ extension (parse_section, min_columns=True)

Backward-compatibility lock
---------------------------
* parse_section on a non-WQ rpt returns the same 5-field rows as before
  (``pollutant_loads: {}``) — byte-identical to pre-WQ behaviour for callers
  that only read the 5 core fields.
"""
from __future__ import annotations

import unittest
from pathlib import Path

from agentic_swmm.agent.swmm_runtime.rpt_summary import (
    SECTIONS,
    parse_section,
    parse_variable_section,
)

REPO_ROOT = Path(__file__).resolve().parents[1]
WQ_RPT = REPO_ROOT / "tests" / "fixtures" / "wq" / "wq_smoke.rpt"


@unittest.skipUnless(WQ_RPT.exists(), f"WQ fixture not found at {WQ_RPT}")
class RunoffQualityContinuityTests(unittest.TestCase):
    @classmethod
    def setUpClass(cls) -> None:
        cls.rpt_text = WQ_RPT.read_text(encoding="utf-8")
        cls.rows = parse_variable_section(cls.rpt_text, SECTIONS["Runoff Quality Continuity"])

    def test_section_returns_rows(self) -> None:
        self.assertGreater(len(self.rows), 0, "Expected at least one row")

    def test_row_has_metric_and_values_keys(self) -> None:
        for row in self.rows:
            self.assertIn("metric", row)
            self.assertIn("values", row)

    def test_pollutant_tss_in_all_rows(self) -> None:
        for row in self.rows:
            self.assertIn("TSS", row["values"], f"TSS missing from row: {row}")

    def test_continuity_error_row_present(self) -> None:
        metrics = {r["metric"] for r in self.rows}
        self.assertIn("Continuity Error (%)", metrics)

    def test_continuity_error_value_is_float(self) -> None:
        for row in self.rows:
            if row["metric"] == "Continuity Error (%)":
                self.assertIsInstance(row["values"]["TSS"], float)

    def test_surface_runoff_row_positive(self) -> None:
        """Surface Runoff should be > 0 for a rainfall-driven run."""
        for row in self.rows:
            if row["metric"] == "Surface Runoff":
                self.assertGreater(row["values"]["TSS"], 0.0)
                return
        self.fail("'Surface Runoff' row not found in Runoff Quality Continuity")


