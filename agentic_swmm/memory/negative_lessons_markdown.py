"""Markdown-backed negative lessons with lifecycle (PRD-06 Phase C.2 follow-up).

The original ``negative_lessons.jsonl`` store records *what failed* —
parameter sets that posted FAIL continuity, calibrations that diverged,
or non-physical parameter regions. Round 7 promotes that data to a
markdown store that mirrors the existing ``lessons_learned.md``
lifecycle (HTML-comment-fenced YAML metadata, exponential confidence
decay, active/dormant/retired buckets, archive on retire).

Why two formats?
----------------
The JSONL store stays as the v1 wire format and as the per-run
"append exactly one row" path. The markdown store is the *curatable*
surface — a human can grep ``## continuity_fail_saanich_b8`` in their
editor, see the metadata fence, and read the body without spinning
up a Python REPL. The lifecycle (decay → dormant → retired → archive)
runs against the markdown only, mirroring ``lessons_lifecycle.py``.

File layout
-----------
``memory/modeling-memory/negative_lessons.md`` has one ``## <name>``
heading per recorded failure, immediately followed by a fenced YAML
preamble. Example::

    ## continuity_fail_saanich_b8

    <!-- aiswmm-negative-lesson-metadata
    metadata:
      case: saanich-b8
      lesson_type: continuity_fail
      parameters_tried:
        manning_n_overland: 0.25
      note: postflight FAIL on runoff_continuity_pct
      first_seen_utc: '2026-05-19T10:00:00Z'
      last_seen_utc: '2026-05-19T10:00:00Z'
      evidence_count: 1
      evidence_runs:
        - 20260519-100000_saanich
      status: active
      confidence_score: 1.0
      half_life_days: 90
    /aiswmm-negative-lesson-metadata -->

    Free-form body text the curator may add later.

Atomicity
---------
Every write follows the tmp-file + ``os.replace`` rename pattern so a
torn write leaves the previous content intact. The fence sentinels are
deliberately distinct from ``lessons_learned.md``
(``aiswmm-negative-lesson-metadata`` vs ``aiswmm-metadata``) so the two
parsers cannot accidentally cross-contaminate when both files are
read by a future indexer.
"""

from __future__ import annotations

import math
import os
import re
import tempfile
from dataclasses import dataclass, field
from datetime import datetime, timezone
from pathlib import Path
from typing import Any, Iterable

import yaml


SCHEMA_VERSION = "1.0"

DEFAULT_HALF_LIFE_DAYS = 90
DEFAULT_ACTIVE_THRESHOLD = 0.5
DEFAULT_DORMANT_THRESHOLD = 0.2

METADATA_OPEN = "<!-- aiswmm-negative-lesson-metadata"
METADATA_CLOSE = "/aiswmm-negative-lesson-metadata -->"

# ``## <name>`` headings whose name is a snake_case identifier are the
# canonical section heads. Names are generated by callers (e.g.
# ``continuity_fail_<case>``) and round-trip back to the dataclass.
_HEADING_RE = re.compile(
    r"^##\s+(?P<name>[a-z][a-z0-9_]*)\s*$", re.MULTILINE
)

_FENCE_RE = re.compile(
    re.escape(METADATA_OPEN)
    + r"\n(?P<payload>.*?)\n"
    + re.escape(METADATA_CLOSE),
    re.DOTALL,
)

_VALID_LESSON_TYPES = frozenset(
    {"continuity_fail", "calibration_diverged", "non_physical_param"}
)


class _StringTimestampLoader(yaml.SafeLoader):
    """SafeLoader that keeps ISO-8601 timestamps as strings.

    Mirrors the behaviour in :mod:`lessons_metadata` so reads and
    writes stay symmetric across both lifecycle modules.
    """


_StringTimestampLoader.yaml_implicit_resolvers = {
    key: [
        (tag, regexp)
        for tag, regexp in resolvers
        if tag != "tag:yaml.org,2002:timestamp"
    ]
    for key, resolvers in _StringTimestampLoader.yaml_implicit_resolvers.items()
}


@dataclass(frozen=True)
class NegativeLessonMd:
    """One markdown-backed negative lesson.

    Frozen so the lifecycle helpers cannot scribble on a record between
    the parser handing it back and the caller acting on it. ``name`` is
    the snake_case section heading; the writer derives it from
    ``(lesson_type, case)`` when callers do not pass one explicitly.
    """

    name: str
    case: str
    lesson_type: str
    parameters_tried: dict[str, float] = field(default_factory=dict)
    note: str = ""
    first_seen: str = ""
    last_seen: str = ""
    evidence_count: int = 0
    evidence_runs: list[str] = field(default_factory=list)
    status: str = "active"
    confidence_score: float = 1.0
    half_life_days: int = DEFAULT_HALF_LIFE_DAYS

    def to_metadata_dict(self) -> dict[str, Any]:
        """Serialise the metadata fence payload (sans ``name``)."""
        return {
            "case": self.case,
            "lesson_type": self.lesson_type,
            "parameters_tried": dict(self.parameters_tried),
            "note": self.note,
            "first_seen_utc": self.first_seen,
            "last_seen_utc": self.last_seen,
            "evidence_count": int(self.evidence_count),
            "evidence_runs": list(self.evidence_runs),
            "status": self.status,
            "confidence_score": round(float(self.confidence_score), 3),
            "half_life_days": int(self.half_life_days),
        }


# ---------------------------------------------------------------------------
# Helpers (internal)


def _now_utc_iso(now: datetime | None = None) -> str:
    current = (now or datetime.now(timezone.utc)).astimezone(timezone.utc)
    return current.isoformat(timespec="seconds").replace("+00:00", "Z")


def _parse_iso(stamp: str) -> datetime:
    text = (stamp or "").strip()
    if not text:
        raise ValueError("empty timestamp")
    if text.endswith("Z"):
        text = text[:-1] + "+00:00"
    parsed = datetime.fromisoformat(text)
    if parsed.tzinfo is None:
        parsed = parsed.replace(tzinfo=timezone.utc)
    return parsed.astimezone(timezone.utc)


def _ensure_header(store: Path) -> None:
    if store.is_file():
        return
    store.parent.mkdir(parents=True, exist_ok=True)
    store.write_text(
        f"<!-- schema_version: {SCHEMA_VERSION} -->\n"
        "# Negative Lessons\n"
        "\n"
        "Parameter regions that consistently fail. Each section carries a\n"
        "metadata fence describing when the lesson was first/last seen,\n"
        "what parameters were tried, how many runs confirmed it, and the\n"
        "lifecycle status (active / dormant / retired).\n"
        "\n",
        encoding="utf-8",
    )


def _atomic_write(path: Path, text: str) -> None:
    """Write ``text`` to ``path`` atomically via tmp + ``os.replace``."""
    path.parent.mkdir(parents=True, exist_ok=True)
    fd, tmp = tempfile.mkstemp(
        prefix=path.name + ".", suffix=".tmp", dir=str(path.parent)
    )
    try:
        with os.fdopen(fd, "w", encoding="utf-8") as handle:
            handle.write(text)
        os.replace(tmp, path)
    except Exception:
        try:
            os.unlink(tmp)
        except OSError:
            pass
        raise


def _iter_sections(text: str) -> Iterable[tuple[str, int, int]]:
    """Yield ``(name, start, end)`` spans for each ``## <section>`` block."""
    matches = list(_HEADING_RE.finditer(text))
    for i, match in enumerate(matches):
        name = match.group("name")
        start = match.start()
        end = matches[i + 1].start() if i + 1 < len(matches) else len(text)
        yield name, start, end


def _parse_metadata(block: str) -> dict[str, Any] | None:
    match = _FENCE_RE.search(block)
    if not match:
        return None
    try:
        parsed = yaml.load(match.group("payload"), Loader=_StringTimestampLoader)
    except yaml.YAMLError:
        return None
    if not isinstance(parsed, dict):
        return None
    meta = parsed.get("metadata")
    if not isinstance(meta, dict):
        return None
    return meta


def _format_metadata_yaml(meta: dict[str, Any]) -> str:
    canonical = [
        "case",
        "lesson_type",
        "parameters_tried",
        "note",
        "first_seen_utc",
        "last_seen_utc",
        "evidence_count",
        "evidence_runs",
        "status",
        "confidence_score",
        "half_life_days",
    ]
    ordered: dict[str, Any] = {}
    for key in canonical:
        if key in meta:
            ordered[key] = meta[key]
    for key in sorted(k for k in meta if k not in canonical):
        ordered[key] = meta[key]
    body = yaml.safe_dump(
        {"metadata": ordered},
        sort_keys=False,
        default_flow_style=False,
        allow_unicode=True,
    ).rstrip()
    return body


def _record_to_block(record: NegativeLessonMd) -> str:
    """Compose the ``## <name> ... <fence>`` block text."""
    payload = _format_metadata_yaml(record.to_metadata_dict())
    fence = f"{METADATA_OPEN}\n{payload}\n{METADATA_CLOSE}"
    return f"## {record.name}\n\n{fence}\n"


def _record_from_metadata(name: str, meta: dict[str, Any]) -> NegativeLessonMd:
    return NegativeLessonMd(
        name=name,
        case=str(meta.get("case") or ""),
        lesson_type=str(meta.get("lesson_type") or ""),
        parameters_tried={
            str(k): float(v)
            for k, v in (meta.get("parameters_tried") or {}).items()
            if _is_number(v)
        },
        note=str(meta.get("note") or ""),
        first_seen=str(meta.get("first_seen_utc") or ""),
        last_seen=str(meta.get("last_seen_utc") or ""),
        evidence_count=int(meta.get("evidence_count") or 0),
        evidence_runs=[str(r) for r in (meta.get("evidence_runs") or [])],
        status=str(meta.get("status") or "active"),
        confidence_score=float(meta.get("confidence_score") or 0.0),
        half_life_days=int(meta.get("half_life_days") or DEFAULT_HALF_LIFE_DAYS),
    )


def _is_number(value: Any) -> bool:
    try:
        float(value)
        return True
    except (TypeError, ValueError):
        return False


def _classify(score: float) -> str:
    if score > DEFAULT_ACTIVE_THRESHOLD:
        return "active"
    if score > DEFAULT_DORMANT_THRESHOLD:
        return "dormant"
    return "retired"


