# Public API


def add_negative_lesson(store: Path, lesson: NegativeLessonMd) -> None:
    """Append ``lesson`` to ``store``; bump evidence on duplicate name.

    When ``store`` does not contain a ``## <lesson.name>`` block we
    append a new section with the metadata fence. When a section with
    the same name already exists we:

      * Increment ``evidence_count`` by 1.
      * Set ``last_seen_utc`` to the lesson's ``last_seen`` (or now
        when the lesson left it empty).
      * Append every entry of ``lesson.evidence_runs`` that is not yet
        in the existing list.
      * Re-compute ``confidence_score`` from the updated
        ``evidence_count`` and ``last_seen_utc`` so the bucket stays
        consistent with the lifecycle rules.

    The body text outside the metadata fence is preserved verbatim;
    only the fence is rewritten. Atomic — a crashed write leaves the
    previous content intact.
    """
    if lesson.lesson_type not in _VALID_LESSON_TYPES:
        raise ValueError(
            f"NegativeLessonMd.lesson_type must be one of "
            f"{sorted(_VALID_LESSON_TYPES)}; got {lesson.lesson_type!r}"
        )
    if not lesson.case.strip():
        raise ValueError("NegativeLessonMd.case must be a non-empty string")

    _ensure_header(Path(store))
    text = Path(store).read_text(encoding="utf-8")

    now_iso = lesson.last_seen or _now_utc_iso()
    first_iso = lesson.first_seen or now_iso

    spans = list(_iter_sections(text))
    existing_span = next((span for span in spans if span[0] == lesson.name), None)

    if existing_span is None:
        record = NegativeLessonMd(
            name=lesson.name,
            case=lesson.case,
            lesson_type=lesson.lesson_type,
            parameters_tried=dict(lesson.parameters_tried),
            note=lesson.note,
            first_seen=first_iso,
            last_seen=now_iso,
            evidence_count=max(1, int(lesson.evidence_count)),
            evidence_runs=list(lesson.evidence_runs),
            status=lesson.status,
            confidence_score=float(lesson.confidence_score) or float(
                max(1, int(lesson.evidence_count))
            ),
            half_life_days=int(lesson.half_life_days),
        )
        block = _record_to_block(record)
        if not text.endswith("\n"):
            text += "\n"
        if not text.endswith("\n\n"):
            text += "\n"
        new_text = text + block
        _atomic_write(Path(store), new_text)
        return

    name, start, end = existing_span
    block = text[start:end]
    meta = _parse_metadata(block) or {}
    existing = _record_from_metadata(name, meta)

    new_runs = list(existing.evidence_runs)
    for run_id in lesson.evidence_runs:
        if run_id and run_id not in new_runs:
            new_runs.append(run_id)

    new_count = existing.evidence_count + 1
    half_life = existing.half_life_days or DEFAULT_HALF_LIFE_DAYS
    new_score = float(new_count) * math.exp(0.0)  # last_seen == now → age 0
    updated = NegativeLessonMd(
        name=name,
        case=existing.case or lesson.case,
        lesson_type=existing.lesson_type or lesson.lesson_type,
        parameters_tried=existing.parameters_tried or dict(lesson.parameters_tried),
        note=existing.note or lesson.note,
        first_seen=existing.first_seen or first_iso,
        last_seen=now_iso,
        evidence_count=new_count,
        evidence_runs=new_runs,
        status=_classify(new_score),
        confidence_score=new_score,
        half_life_days=half_life,
    )

    # Preserve body text between the metadata fence and the next ``##``.
    fence_match = _FENCE_RE.search(block)
    if fence_match is None:
        # Block without a fence — rebuild from scratch.
        new_block = _record_to_block(updated)
    else:
        new_payload = _format_metadata_yaml(updated.to_metadata_dict())
        new_fence = f"{METADATA_OPEN}\n{new_payload}\n{METADATA_CLOSE}"
        new_block = (
            block[: fence_match.start()] + new_fence + block[fence_match.end():]
        )

    new_text = text[:start] + new_block + text[end:]
    _atomic_write(Path(store), new_text)


def list_negative_lessons(
    store: Path, *, status: str | None = None
) -> list[NegativeLessonMd]:
    """Parse every ``## <name>`` section into typed records.

    ``status`` filters to a single bucket (``"active"``, ``"dormant"``,
    ``"retired"``); ``None`` returns all. Missing files yield ``[]``.
    Sections without a metadata fence are skipped silently — they may
    represent free-form curator notes that were appended after the
    writer's last pass.
    """
    store = Path(store)
    if not store.is_file():
        return []
    text = store.read_text(encoding="utf-8")
    out: list[NegativeLessonMd] = []
    for name, start, end in _iter_sections(text):
        meta = _parse_metadata(text[start:end])
        if meta is None:
            continue
        record = _record_from_metadata(name, meta)
        if status is not None and record.status != status:
            continue
        out.append(record)
    return out


def is_param_set_known_bad_md(
    store: Path,
    case_name: str,
    params: dict[str, float],
    *,
    tolerance_pct: float = 5.0,
) -> NegativeLessonMd | None:
    """Mirror of ``is_param_set_known_bad`` against the markdown store.

    A match requires the candidate ``params`` cover every recorded key
    and every covered value lie within ``tolerance_pct`` percent of the
    recorded value (relative to the recorded magnitude). Retired
    lessons are skipped — they have decayed below the dormant threshold
    so we no longer use them to block new proposals.
    """
    if tolerance_pct < 0:
        raise ValueError("tolerance_pct must be non-negative")

    for lesson in list_negative_lessons(store):
        if lesson.case != case_name:
            continue
        if lesson.status == "retired":
            continue
        recorded = lesson.parameters_tried
        if not recorded:
            continue
        if not all(key in params for key in recorded):
            continue
        within = True
        for key, recorded_value in recorded.items():
            candidate = params[key]
            denom = max(abs(float(recorded_value)), 1e-9)
            pct = (
                abs(float(candidate) - float(recorded_value)) / denom * 100.0
            )
            if pct > tolerance_pct:
                within = False
                break
        if within:
            return lesson
    return None


def apply_decay(
    store: Path, *, now: str | None = None
) -> dict[str, int]:
    """Apply exponential decay across every section, returning bucket counts.

    The decay formula matches ``lessons_metadata.compute_confidence``:
    ``score = evidence_count * exp(-age_days / half_life_days)``. Age is
    computed from ``last_seen_utc`` against ``now`` (or the current UTC
    time when ``now`` is None). Status transitions:

      * score > 0.5  → ``active``
      * 0.2 < score ≤ 0.5 → ``dormant``
      * score ≤ 0.2  → ``retired``

    Returns a dict with bucket counts (``active``, ``dormant``,
    ``retired``) plus a ``moved`` count that totals the sections whose
    status changed during this pass.
    """
    store = Path(store)
    counts = {"active": 0, "dormant": 0, "retired": 0, "moved": 0}
    if not store.is_file():
        return counts

    current = _parse_iso(now) if now else datetime.now(timezone.utc)
    text = store.read_text(encoding="utf-8")
    spans = list(_iter_sections(text))
    if not spans:
        return counts

    new_text = text
    # Walk in reverse so byte offsets stay stable after substitutions
    # past the current span.
    for name, start, end in reversed(spans):
        block = new_text[start:end]
        meta = _parse_metadata(block)
        if meta is None:
            continue
        record = _record_from_metadata(name, meta)
        if record.evidence_count <= 0 or record.half_life_days <= 0:
            counts[record.status] = counts.get(record.status, 0) + 1
            continue
        try:
            last_seen = _parse_iso(record.last_seen)
        except ValueError:
            counts[record.status] = counts.get(record.status, 0) + 1
            continue
        age_days = max(
            0.0, (current - last_seen).total_seconds() / 86400.0
        )
        score = float(record.evidence_count) * math.exp(
            -age_days / float(record.half_life_days)
        )
        new_status = _classify(score)
        if new_status != record.status:
            counts["moved"] += 1
        counts[new_status] = counts.get(new_status, 0) + 1

        new_meta = dict(meta)
        new_meta["confidence_score"] = round(score, 3)
        new_meta["status"] = new_status
        new_payload = _format_metadata_yaml(new_meta)
        fence_match = _FENCE_RE.search(block)
        if fence_match is None:
            continue
        new_fence = f"{METADATA_OPEN}\n{new_payload}\n{METADATA_CLOSE}"
        new_block = (
            block[: fence_match.start()] + new_fence + block[fence_match.end():]
        )
        new_text = new_text[:start] + new_block + new_text[end:]

    if new_text != text:
        _atomic_write(store, new_text)
    return counts


def archive_retired(store: Path, archive_store: Path) -> int:
    """Move retired sections out of ``store`` into ``archive_store``.

    Returns the count of sections moved. The archive file is created on
    first use with a small header. The source store has its retired
    sections excised but its other content (header, active/dormant
    sections, curator notes) is preserved.
    """
    store = Path(store)
    archive = Path(archive_store)
    if not store.is_file():
        return 0
    text = store.read_text(encoding="utf-8")
    spans = list(_iter_sections(text))
    retired_blocks: list[str] = []
    keep_text = text

    # Walk in reverse so removal stays index-safe.
    for name, start, end in reversed(spans):
        block = keep_text[start:end]
        meta = _parse_metadata(block)
        if meta is None:
            continue
        if str(meta.get("status") or "active") != "retired":
            continue
        retired_blocks.append(block)
        # Excise; merge seam with a single blank line.
        head = keep_text[:start]
        tail = keep_text[end:]
        if head.endswith("\n\n") and tail.startswith("\n"):
            tail = tail.lstrip("\n")
            head = head.rstrip("\n") + "\n\n"
        keep_text = head + tail

    if not retired_blocks:
        return 0

    _atomic_write(store, keep_text)

    if not archive.is_file():
        archive.parent.mkdir(parents=True, exist_ok=True)
        archive.write_text(
            f"<!-- schema_version: {SCHEMA_VERSION} -->\n"
            "# Negative Lessons (Archived)\n"
            "\n"
            "Sections that decayed below the dormant threshold. Move a\n"
            "section back to ``negative_lessons.md`` to revive its\n"
            "lifecycle.\n"
            "\n",
            encoding="utf-8",
        )
    existing = archive.read_text(encoding="utf-8")
    if not existing.endswith("\n"):
        existing += "\n"
    if not existing.endswith("\n\n"):
        existing += "\n"
    # retired_blocks was built in reverse; flip back to insertion order.
    appended = existing + "".join(reversed(retired_blocks))
    _atomic_write(archive, appended)
    return len(retired_blocks)


def migrate_jsonl_to_md(jsonl_path: Path, md_path: Path) -> int:
    """Convert ``jsonl_path`` rows to ``md_path`` sections.

    Idempotent: rows whose derived section name already exists in
    ``md_path`` are bumped via the same ``add_negative_lesson`` path
    used by the audit hook. Returns the count of *new* sections
    written; updates to pre-existing sections do not increment.

    The section name is derived as ``"<lesson_type>_<case_slug>"``
    where ``case_slug`` is the case name with non-alphanumeric
    characters replaced by ``_``. This keeps every section heading a
    valid snake_case identifier that the parser can locate.
    """
    from agentic_swmm.memory.negative_lessons import recall_negative_lessons

    jsonl = Path(jsonl_path)
    md = Path(md_path)
    if not jsonl.is_file():
        return 0
    existing_names = {lesson.name for lesson in list_negative_lessons(md)}
    rows = recall_negative_lessons(jsonl, {})
    new_count = 0
    for row in rows:
        name = _section_name(row.lesson_type, row.case_name)
        recorded_at = row.recorded_at or _now_utc_iso()
        record = NegativeLessonMd(
            name=name,
            case=row.case_name,
            lesson_type=row.lesson_type,
            parameters_tried=dict(row.parameters_tried),
            note=row.note,
            first_seen=recorded_at,
            last_seen=recorded_at,
            evidence_count=1,
            evidence_runs=[row.run_id] if row.run_id else [],
            status="active",
            confidence_score=1.0,
            half_life_days=DEFAULT_HALF_LIFE_DAYS,
        )
        was_new = name not in existing_names
        add_negative_lesson(md, record)
        if was_new:
            new_count += 1
            existing_names.add(name)
    return new_count


def _section_name(lesson_type: str, case_name: str) -> str:
    case_slug = re.sub(r"[^a-z0-9]+", "_", (case_name or "").lower()).strip("_")
    lt = re.sub(r"[^a-z0-9]+", "_", (lesson_type or "").lower()).strip("_")
    name = f"{lt}_{case_slug}" if case_slug else lt
    if not re.match(r"^[a-z]", name):
        name = "lesson_" + name
    return name


